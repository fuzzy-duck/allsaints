                    The Reverend John Snowdon, Vicar of Ilkley from 1842 to 1878, placed the shafts on the south side of
                    the churchyard, probably as part of the 1861 rebuilding. In 1855 they had been observed stuck in the
                    ground, the tallest shaft being only five foot high, preserving the other four feet under ground.

                </p>


                <p>
                    In 1914 a cross piece was reconstructed from two original pieces and placed on the tallest shaft.

                </p>
