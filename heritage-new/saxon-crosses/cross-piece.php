                <p>
                    4. The Cross-Piece. The reconstructed cross-piece comprises of two pieces. The lower stone was
                    preserved at the "Calvary" constructed by Mr Peter Middleton, who died in 1886. It is described by
                    J. Romilly Allen in his paper to the Yorkshire Archaeological Society excursion to Ilkley in 1884.
                    The upper stone was found in the River Wharfe, also in 1884.

                </p>
                <p>
                    In June 1914, the two stones were presented to the Vicar and Churchwardens by Mr Sydney Kellet of
                    Middleton Lodge and soon fixed to the tallest shaft, still on display on the south side of the
                    church, under the direction of Mr H.B. McCall, F.S.A.

                </p>
                <p>
                    While Professor Collingwood had written to the church in Ilkley in May 1914, together with his
                    sketch of how he believed they had once been (see illustration), he wrote later that year: " the
                    lower fragment is twenty four inches across the arms, with spatulated arm-ends, eight and a half
                    inches tapering to eight inches broad. The stone is like that of the shafts. The lateral arms show
                    birds pecking fruit, of which the tendrils do not seem to be drawn with the naturalism of earlier
                    Anglian art. The boss, surrounded by three rings, has been chipped off. Perhaps originally they
                    contained a jewel.

                </p>
                <p>
                    The rude angel on the upper stone and the moulding brings this stone to the period of the tallest
                    shaft. It could not possibly have belonged to the shortest of the Crosses."

