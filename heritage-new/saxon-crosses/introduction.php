                <p>
                    Monolithic pillars have long been associated with the fertility rites of ancient natural religions.

                </p>
                <p>
                    Those of the Canaanites in Bible times are examples of what was widespread elsewhere. With the
                    conversion of Roman Emperor Constantine to Christianity in 320 A.D., such pillars were capped with
                    crosses to become symbols of purity and landmarks for the preaching of the gospel. There then
                    followed the skilled crafting of Christian crosses. Many were associated with the travels of
                    evangelist Paulinus in the early 7th century. Others were erected as sepulchral monuments for people
                    of substance and station.

                </p>
                <p>
                    That IIkley has three such crosses is remarkable. Consider the quarrying of these large shafts of
                    millstone grit locally, then moving them with great difficulty to the masons' yard without breaking
                    them, then shaping them, deciding on the design and finally colouring them with red lead and other
                    bright pigments. Finally imagine putting each of them into position on a special stone base with the
                    cross-piece carefully slotted into position. What powerful visual symbols they would be in a non
                    literate age.

                </p>
                <p>
                    Here on one of the IIkley Crosses we have the first visual representation of the Lord Jesus Christ
                    in IIkley, Christ seated in glory with His right hand upraised in blessing and holding a pastoral
                    staff. On the same pillar are the four gospel writers. I cannot imagine the three shafts being
                    carved at the same period, but rather in succession over a hundred years by succeeding generations
                    of masons assisted by travelling masons with design ideas from elsewhere.

                </p>
                <p>
                    The expert can "read" the designs -the style of carving, the figures, the beasts, the scrolls, the
                    leaves, the fruits and the Christian symbols. J. Romilly Allen has done a painstaking analysis of
                    all the stone knots and designs, not only at 66 sites in West Yorkshire but also much further
                    afield. He has found common ideas and has put designs into area groupings.

                </p>
