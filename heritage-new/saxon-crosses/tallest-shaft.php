                <h1>TALLEST SHAFT</h1>

                <!--<img src="/images/image8.gif" alt="" />-->

                <p>1. The tallest shaft shows the Lord Jesus Christ enthroned in glory. He is seated, not easy to show
                    in carved flat relief. That He is seated reminds us that His sacrificial death has been accepted
                    that our sins be forgiven. In His left hand is a palm branch or pastoral staff. His right hand, now
                    defaced, is raised in blessing (as at Dewsbury).</p>

                <!--<img src="/images/image9.gif" alt="" />-->

                <p>
                    Below Him are various beasts. In the panel are two dragonesque beasts with tails intertwined and
                    biting their own tails. Serpent like in body, they have no limbs. Beneath them is a single beast in
                    profile with raised paw.

                </p>
                <p>
                    The raised leg or paw is a feature of the IIkley shafts.

                </p>
                <p>
                    The beast in the lowest panel stands on its hind legs, also with raised front leg.

                </p>
                <p>
                    Scholars have likened these panels to the Rothwell Cross in Dumfriesshire where there is an
                    inscription from The Apocryphal gospel of the Nativity:

                </p>
                <p>
                    " Jesus Christ, the Judge of Righteousness; beasts and dragons knew the Saviour of the world in the
                    wilderness and came and worshipped Him. ..

                </p>


                <p>
                    The reverse side of this tallest cross clearly presents the four Gospel writers of the New Testament
                    in the Bible, Matthew, Mark, Luke and John. From the 3rd century to the 13th century, there are
                    representations of Jesus Christ surrounded by His evangelists. In the catacombs of Rome, Christ is
                    represented by a Cross and the evangelists bya scroll at each corner.

                </p>
                <p>
                    As early as the 6th century the evangelists are symbolised by the four beasts in chapter 4 of the
                    Book of Revelation. Of particular note on the Ilkley Crosses is the fact that the evangelists have
                    the heads of beasts and the bodies of humans. They are holding a book, not a scroll. This deviation
                    is only found elsewhere on a Saxon slab at Wirksworth Church in Derbyshire and in one or two ancient
                    manuscripts.

                </p>
                <p>
                    The description of an Irish stone cross at Clonmacnois as " The Cross of the Scriptures" is equally
                    applicable here.

                </p>
                <p>
                    <strong>The side panels each have their features. The scrolls have rounded stems and more
                        conventional leaves with fruit on the one side growing out of dragonesque animals' (possibly
                        snakes) with interlaced tails. On the other side there is a tree form on top of two spirals.
                        Collingwood sees a phallic symbol which has escaped the notice of previous archaeolgists. Others
                        see a snake at the root of the tree of Yggdrasil mentioned by the Viking poet.</strong>

                </p>
                <p>
                    There are two triquetra above the snakes, three cornered knots, symbolic of God the Holy Trinity,
                    Father, Son and Holy Spirit. The opposite side has two pairs of three leaves.

                </p>
                <p>
                    Collingwood dates this shaft at 850 A.D. but more recently scholars from York suggest a date of a
                    round 800 A.D.

                </p>
