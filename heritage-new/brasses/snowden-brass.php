                <p>
                    1. Jasper Whitfield Snowden. Born 1844 in the temporary vicarage in Wells Road, the same year as his
                    sister Elizabeth died, aged 4. (She is remembered in the stained glass window in the north chancel.)
                    Jasper's mother Margaret, daughter of Jasper Whitfield of Stockton, died in 1853 leaving Jasper and
                    his three brothers and three sisters with their sorrowing father, John. Jasper, with his brothers
                    William and Edward, caught their father's great enthusiasm for bell ringing. When Jasper was aged
                    one, the three ancient and cracked bells were recast to make a peal of six bells. In 1873 Jasper,
                    29, and his brothers raised £300 out of a needed £350 to add two more bells, a Treble and a Tenor
                    (18 cwt.). One bell was recast to make a semi-tone. Jasper died on November 16th, 1885 aged 41, a
                    historian and expositor of change-ringing of national fame. Muffled peals of bells were rung in
                    towers throughout the land in his honour and memory.

                </p>
                <p>
                    2. William Snowden, born in 1846, the family still being in the temporary vicarage, took over at
                    once as President of the Yorkshire Change-Ringers, on the death of his brother Jasper. Aged 39, he
                    continued as president for 25 years until 1910. He died aged 69 in Cartmell in 1915. The dedication
                    "Holiness to the Lord" shows the commitment to God and his church of his life's work of calling
                    people to worship through bell ringing.

                </p>
                <p>
