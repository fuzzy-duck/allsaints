                    <strong>The Revd. John Snowden Vicar 1842 -1878</strong>

                </p>

                <p>
                    Born near Durham on February 6th, 1806, John Snowden moved to Ilkley on August 4th, 1842, with his
                    wife Margaret whom he had met while curate in Stockton. The "ancient vicarage" was in disrepair.
                    With a grant from Queen Anne's Bounty and his own large donation, a new parsonage was built on Wells
                    Road in 1848. The present font was put in the garden, as well as two stone crosses. A man of great
                    energy and humour, he embarked on two major building projects simultaneously, the enlarging of the
                    church at a cost of £1,300, re-opened on May 6th, 1861 and with the support of Canon Jackson of
                    Leeds, the Ilkley Hospital, completed in 1862 at a cost of £3,016. There were fifty beds. The first
                    matron was Mrs Dean. The hospital admitted 449 patients in its first year.

                </p>
                <p>
                    The railway came to Ilkley in 1865.

                </p>
                <p>
                    His next project was "the All Saints' National Schools" which opened in July 1872. The school could
                    accommodate 440 children and cost £3,000. The final £923 was raised by a 3 day Bazaar. Plans were in
                    hand in June 1872 for moving the Grammar School from Skipton Road to enlarged premises to hold 60
                    boys, with 6 free places and a boarding house. Parson Snowden delayed this project for some years,
                    arguing about which site was to be used for the new Grammar School. His death in 1878 solved that
                    problem.

                </p>
                <p>
                    He built The Church Institute, as a church hall, in 1875 in Leeds Road, now used by The Operatic
                    Society. He was one of the trustees planning a new church for Ilkley. He died on February 5th 1878,
                    so did not see the opening of St Margaret's in 1879. While dedicated to St Margaret of Antioch, it
                    was said to be named after the vicar's daughter, Margaret. John Snowden died after a fall down the
                    vicarage stairs, where he lived with one of his daughters. It was written at the time, "Anyone more
                    gentle, more genial, more courageous, more self-forgetful, you will not meet. You may find a more
                    energetic man, but never one wiser". A widower for 25 years, he was 72 when he died.

                </p>
