                    1743 until his death. He married well. Elizabeth Blanchard was daughter of George Blanchard,
                    Gentleman, of York. They had no children. She died in September, 1764.

                </p>
                <p>
                    In 1758 Parson Beeston became Master of Ilkley Grammar School. However, the townsfolk put up a
                    petition to "His Grace the Lord Archbishop of York" complaining that as schoolmaster Beeston was
                    "very careless and negligent...the children had little or no education...an evil example...does
                    damage to us and our children...takes the salary....... cause him to resign". The petition was
                    signed by 39 men and one woman, Mary Hauxworth.

                </p>
                <p>
                    However, he put the vicarage into good repair, built a gable and in the 1770s, as a widower "held a
                    housewarming party to which the pick of Ilkley came in their wigs and farthingales (hooped skirts)."

                </p>
                <p>
                    Beeston died of gout on March 12th, 1777. He was buried in Church in 1778. George Benson, curate,
                    lodged at the Vicarage. Benson later became vicar. Beeston was buried in the north aisle adjoining
                    the triple decker pulpit. This pulpit was taken down in 1830. He was known as a fine classical
                    scholar, hence the inscriptions on his brass in Latin and Greek.

