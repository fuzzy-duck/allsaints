<h1>THE CHURCH: A BRIEF GUIDE</h1>

<p><strong>Like many other sites across the UK (such as Ribchester and Holyhead), Ilkley Parish Church stands on the ground of an old Roman fort.</strong></p>

<p>Evidence from this era suggests that this abandoned fort of Olicana had a southern rampart that ran a little to the south of what is now Church Street, while the eastern rampart extended to the east of Lower Brook Street. </p>

<p>But the ruins aren’t all hidden beneath the surface. It’s thought that material from the fort was actually used and re-used in the many transformations the church has undergone over the centuries. </p>

<h2>Medieval and Victorian modifications</h2>

<p>Although the current exterior of the church retains the general appearance and proportions of a late medieval building, it actually dates back to 1860. This is mainly due to a large rebuild in the Victorian era. However, there are still some remnants of the medieval era, including:</p>

<p><strong>The fifteenth century tower.</strong> Aside from a window on the north face and the small doorway on the south side, this seems to be in its original state – with enormous blocks of gritstone used in its construction and the curious little niche high on the south-east buttress.</p>

<p><strong>Some of the north walls of the church.</strong> The aisle wall, in particular, is thought to be of the fourteenth century with fifteenth-century windows and a doorway added. While the clerestory wall appears to be medieval, with later window additions in 1880. You can see the difference between the rubble construction of the older section and the regular coursing of the Victorian wall further east. </p>


