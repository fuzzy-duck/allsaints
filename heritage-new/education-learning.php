            <h1>Bringing History Alive!</h1>
                <p>All Saints Church holds an important place in the history of Ilkley and has a unique story to tell that began nearly 1400 years ago.</p>
                <p>It is the perfect place to discover something new, whatever your age. We hope that all visitors to Ilkley in the future will come to visit the Church, the oldest building in the town. We are proud custodians of the rich heritage visible in the fabric of this building, and as lived through the lives of the people who worshipped here through the centuries. The award of a Heritage Lottery Grant in 2018, part of the wider church development project, has seen the introduction of new information boards and multimedia displays that tell the story of the church and the important part it has played in the development of Ilkley from the time of the Romans to the present day.</p>
                <p>Come and explore the Church and see how the Heritage team can help you delve into the past and enjoy the Church from many angles. We are here to enable children, young people, families and the local community to enjoy, explore and learn from the rich and special heritage that our church offers.</p>

